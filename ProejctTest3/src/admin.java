    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String nama = namaTF.getText();
        String umur = umurTF.getText();
        String telefon = telefonTF.getText();
        String surah = surahCB.getSelectedItem().toString();
        participants.add(new Assignment3(nama,umur,telefon,surah));
        dtm.setRowCount(0);
        
        try{
           FileWriter Writer = new FileWriter ("NAMA PESERTA.txt",true);
           Writer.write("  "+nama+"  "+umur+"  "+telefon+"  "+surah); 
           Writer.write(System.getProperty("line.separator"));
           Writer.close();
           JOptionPane.showMessageDialog(null, "DATA ANDA BERJAYA DIMASUKAN!");
           setVisible(true);
           new FileIOandArraylist().setVisible(false);
           
        }catch(Exception e){
           JOptionPane.showMessageDialog(null, "DATA ANDA GAGAL DIMASUKAN!");
            
        }
        
        for(int i =0; i<participants.size(); i++) {
            Object [] obj = {participants.get(i).nama,participants.get(i).umur,participants.get(i).telefon,participants.get(i).surah};
            dtm.addRow(obj);
        }
        clearField();
    }
    private void clearField(){
     namaTF.setText("");
     umurTF.setText("");
     telefonTF.setText("");
     surahCB.setSelectedIndex(0);
    
    }//GEN-LAST:event_jButton2ActionPerformed

