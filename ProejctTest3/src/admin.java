    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(this, "Search Nama Peserta :");
        for(int i =0; i<participants.size(); i++){
            if(participants.get(i).nama.equalsIgnoreCase(input)){
                JOptionPane.showMessageDialog(jButton5, "JUMPA", "Search Nama", 2);
                namaTF.setText(participants.get(i).nama);
                umurTF.setText(participants.get(i).umur);
                telefonTF.setText(participants.get(i).telefon);
                surahCB.setSelectedItem(participants.get(i).surah);
                String surah = participants.get(i).surah;
                
                for(int j = 0; j<(surahCB.getItemCount());j++){
                    if(surahCB.getItemAt(i).equalsIgnoreCase(surah)){
                        surahCB.setSelectedIndex(i);
                    }
                }
                return;
            }
        }
        JOptionPane.showMessageDialog(jButton5, "TIDAK JUMPA", "Search Nama", 2);
    }//GEN-LAST:event_jButton5ActionPerformed

